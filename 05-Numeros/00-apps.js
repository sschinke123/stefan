// 1. Math.pow() - Potenz
let result = Math.pow(2, 3);  // 2 hoch 3 = 8
console.log("Math.pow(2, 3):", result);  // Gibt 8 aus

// 2. Math.abs() - Absoluter Wert
let absValue = Math.abs(-5);  // Der absolute Wert von -5 ist 5
console.log("Math.abs(-5):", absValue);  // Gibt 5 aus

// 3. Math.min() - Kleinster Wert
let minValue = Math.min(10, 20, 5, 30);  // Der kleinste Wert ist 5
console.log("Math.min(10, 20, 5, 30):", minValue);  // Gibt 5 aus

// 4. Math.max() - Größter Wert
let maxValue = Math.max(10, 20, 5, 30);  // Der größte Wert ist 30
console.log("Math.max(10, 20, 5, 30):", maxValue);  // Gibt 30 aus

// 5. Math.random() - Zufällige Zahl zwischen 0 und 1
